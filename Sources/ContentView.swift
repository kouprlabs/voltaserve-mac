import SwiftData
import SwiftUI
import VoltaserveCore

struct ContentView: View {
    @Environment(\.modelContext) private var modelContext
    @Query private var items: [Item]

    var body: some View {
        NavigationSplitView {
            List {
                ForEach(items) { item in
                    NavigationLink {
                        WorkspaceRow(
                            .init(
                                id: UUID().uuidString,
                                name: item.timestamp.pretty,
                                permission: .owner,
                                storageCapacity: 100_000_000,
                                rootID: UUID().uuidString,
                                organization: .init(
                                    id: UUID().uuidString,
                                    name: "Just My Organization",
                                    permission: .owner,
                                    createTime: Date().description
                                ),
                                createTime: Date().pretty
                            )
                        )
                    } label: {
                        Text(item.timestamp, format: Date.FormatStyle(date: .numeric, time: .standard))
                    }
                }
                .onDelete(perform: deleteItems)
            }
            .navigationSplitViewColumnWidth(min: 180, ideal: 200)
            .toolbar {
                ToolbarItem {
                    Button(action: addItem) {
                        Label("Add Item", systemImage: "plus")
                    }
                }
            }
        } detail: {
            Text("Select an item")
        }
        .font(.custom(VOMetrics.bodyFontFamily, size: VOMetrics.bodyFontSizeMac))
    }

    private func addItem() {
        withAnimation {
            let newItem = Item(timestamp: Date())
            modelContext.insert(newItem)
        }
    }

    private func deleteItems(offsets: IndexSet) {
        withAnimation {
            for index in offsets {
                modelContext.delete(items[index])
            }
        }
    }
}

#Preview {
